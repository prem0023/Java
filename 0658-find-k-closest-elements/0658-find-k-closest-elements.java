class Solution {
    public List<Integer> findClosestElements(int[] arr, int k, int x) {
        int left = 0, right = arr.length - k;
        
        while(left < right){
            int mid = (left+right)/2;
            if(x - arr[mid] <= arr[mid+k] - x)
                right = mid;
            else
                left = mid+1;
        }
        
        List<Integer> ans = new ArrayList<>();
        
        for(int i=left; i< left+k; i++)
            ans.add(arr[i]);
        
        return ans;
    }
}